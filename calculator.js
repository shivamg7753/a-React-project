class  calculator {
    static add(a,b){
        return a+b;
    }
   static subtract(a,b){
        return a-b;
    }
  static  multiply(a,b){
        return a*b;
    }
   static divide(a,b){
        return a/b;
    }
}
 
console.log(calculator.add(2,3));
console.log(calculator.subtract(5,2));
console.log(calculator.multiply(3,4));
console.log(calculator.divide(10,2));

